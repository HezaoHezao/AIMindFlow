package org.aimindflow.common.sensitive.enums;

/**
 * 敏感数据类型枚举
 *
 * @author HezaoHezao
 */
public enum SensitiveType {

    /**
     * 手机号脱敏
     * 保留前3位和后4位，中间用****替换
     * 例如：138****1234
     */
    PHONE,

    /**
     * 邮箱脱敏
     * 保留@前的前2位和@后的全部，中间用****替换
     * 例如：ab****@example.com
     */
    EMAIL,

    /**
     * 身份证号脱敏
     * 保留前4位和后4位，中间用**********替换
     * 例如：1234**********5678
     */
    ID_CARD,

    /**
     * 姓名脱敏
     * 保留第一个字符，其余用*替换
     * 例如：张**
     */
    NAME,

    /**
     * 银行卡号脱敏
     * 保留前4位和后4位，中间用****替换
     * 例如：6222****1234
     */
    BANK_CARD,

    /**
     * 地址脱敏
     * 保留前6个字符，其余用****替换
     * 例如：北京市朝阳区****
     */
    ADDRESS,

    /**
     * 密码脱敏
     * 全部用******替换
     */
    PASSWORD,

    /**
     * IP地址脱敏
     * 保留前两段，后两段用*替换
     * 例如：192.168.*.*
     */
    IP_ADDRESS,

    /**
     * 车牌号脱敏
     * 保留前2位和后1位，中间用***替换
     * 例如：京A***1
     */
    CAR_LICENSE,

    /**
     * 自定义脱敏规则
     * 需要配合@Sensitive注解的rule属性使用
     */
    CUSTOM
}